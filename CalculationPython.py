# Tutorial of Function with return statement


def area_of_circle(radius):
    return 2 * 3.14 * radius


def area_of_rectangle(length, height):
    return length * height


def cube_of_num(num):
    return num * num * num


print("Area of circle is %s " % area_of_circle(5))
print("Area of Rectangle is %s " % area_of_rectangle(10, 20))
print("Cube of number is %s " % cube_of_num(5))
